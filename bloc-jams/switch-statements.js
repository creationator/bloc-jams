theWeekend = function(week) {
	switch (week) {
		case Monday:
		return "Nope";
		break;

		case Tuesday:
		return "Nope again";
		break;

		case Wednesday:
		return "Naw not yet";
		break;

		case Thursday:
		return "One more day";
		break;

		case Friday:
		return "Yeah buddy";
		break;

		case Saturday:
		return "Its the WEEKEND!!!!";
		break;

		case Sunday:
		return "Please dont end";
		break;

		default:
		return "Not even close";
		break;
	}
};
